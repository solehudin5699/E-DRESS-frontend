import * as React from 'react';
import {View, Text} from 'react-native';
import {NavigationContainer} from '@react-navigation/native';
import {createBottomTabNavigator} from '@react-navigation/bottom-tabs';
import {Icon} from 'react-native-elements';
import Home from './src/screens/Home';
import Profile from './src/screens/Profile';

function HomeScreen() {
  return (
    <View style={{flex: 1, justifyContent: 'center', alignItems: 'center'}}>
      <Text>Home!</Text>
    </View>
  );
}

function SettingsScreen() {
  return (
    <View style={{flex: 1, justifyContent: 'center', alignItems: 'center'}}>
      <Text>Settings!</Text>
    </View>
  );
}

const Tab = createBottomTabNavigator();

export default function App() {
  return (
    <NavigationContainer>
      <Tab.Navigator
        tabBarOptions={{
          activeTintColor: 'white',
          inactiveTintColor: '#517fa4',
          tabStyle: {backgroundColor: '#CBE15A', paddingBottom: 5},
          labelStyle: {
            // color: '#517fa4',
            textTransform: 'uppercase',
            fontStyle: 'normal',
            fontWeight: '700',
            fontSize: 11,
            marginTop: -10,
          },
          adaptive: false,
        }}>
        <Tab.Screen
          name="Home"
          component={Home}
          options={{
            tabBarLabel: 'Home',
            tabBarIcon: ({color, size}) => (
              <Icon name="home" size={24} type="material" color={color} />
            ),
          }}
        />
        <Tab.Screen
          name="History"
          component={HomeScreen}
          options={{
            tabBarLabel: 'Riwayat',
            tabBarIcon: ({color, size}) => (
              <Icon name="history" size={24} type="material" color={color} />
            ),
          }}
        />
        <Tab.Screen
          name="Profile"
          component={Profile}
          options={{
            tabBarLabel: 'Profile',
            tabBarIcon: ({color, size}) => (
              <Icon name="person" size={24} type="material" color={color} />
            ),
          }}
        />
      </Tab.Navigator>
    </NavigationContainer>
  );
}
